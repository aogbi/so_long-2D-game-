#include "ft_printf.h"

int	ft_putstr(char *s, int len)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (len + 6);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i + len);
}
